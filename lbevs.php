These extremity vacuum splints are constructed from a high quality puncture resistant, MRI compatible /X-Ray translucent polyurethane material which is able keep a vacuum for 24 hours. 
<p><b>Easy to apply:</b> 

<p>Place the desired extremity vacuum splint on a flat surface; distribute the beads evenly over the extremity splint. 

<p>Apply the small hand vacuum pump, open the valve, and pump out some air, making the splints stiffer to prevent the beads from shifting when applying to the extremity. 

<p>Close the valve; disconnect the vacuum pump and apply the splint to the extremity. 

<p>Form the splint around the extremity and use medical tape to close it, connect the vacuum pump, open the valve and withdraw the air fully. Close the valve and readjust with some more tape, if necessary. 

<p>The disposable extremity vacuum splints folds to a compact size when not in use. A set is available with a small hand-operated vacuum pump, suction-adapter for portable suction units and a carrying case for easy storage.


	
